function setup() {
  createCanvas(windowWidth, windowHeight);
}

function draw() {
  background(220);
  fill("red");
  rect(width/4,height/4, 400, 400);
  fill("white");
  rect(width/4+400,height/4, 400, 400);
  fill("blue");
  circle(width/2, height/2, 100);

}
