public class BigorSmall {
    public void numCheck(int numa, int numb){ 
        System.out.println("Is " + numa + " bigger than " + numb + "?");
        if (numa > numb){
            System.out.println("Yes");
        }
        else if (numa < numb){
            System.out.println("No");
        }
        else{
            System.out.println("Same number");
        }
        
    }
}
